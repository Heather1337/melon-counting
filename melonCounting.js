const countMelons = (melonList) => {
    /* Takes in a list of melons and returns an object with the count by melon type. */

    const melonCount = {};

    for (const melon of melonList) {
        if(!(melon in melonCount)) {
            melonCount[melon] = 1;
        } else {
            melonCount[melon]++;
        }
    }

    return melonCount;
};

